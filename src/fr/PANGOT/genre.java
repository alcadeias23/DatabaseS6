package fr.PANGOT;

public class genre {

	private String g_id;
	private String g_type;
	
	public genre(String g_id, String g_type) {
		super();
		this.g_id = g_id;
		this.g_type = g_type;
	}

	public String getG_id() {
		return g_id;
	}

	public void setG_id(String g_id) {
		this.g_id = g_id;
	}

	public String getG_type() {
		return g_type;
	}

	public void setG_type(String g_type) {
		this.g_type = g_type;
	}
	
	
	
	
}
